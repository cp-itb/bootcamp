void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    for (int i = 0; i < n; i++) {
        if (a[i] >= 0) break;
        if (k == 0) break;
        a[i] = -a[i]; k--;
    }
    sort(a.begin(), a.end());
    if (k > 0 && k % 2 == 1) {
        a[0] = -a[0];
    }
    int sum = 0;
    for (int x : a) sum += x;
    cout << sum << '\n';
}

