const int M = 1e9 + 7;

void solve() {
    int n, m;
    cin >> n >> m;
    vector<int> coins(n);
    for (int i = 0; i < n; i++) {
        cin >> coins[i];
    }
    vector<int> dp(m + 1);
    dp[0] = 1;
    for (int i = 0; i < n; i++) {
        for (int val = coins[i]; val <= m; val++) {
            dp[val] = (dp[val] + dp[val - coins[i]]) % M;
        }
    }
    cout << dp[m] << '\n';
}

